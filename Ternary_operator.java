
public class Ternary_operator {

	public static void main(String[] args) {
		// ternary operator will be indicated by using ?:
		// after writing the literals if true execute the question mark part else execute quotation part
		int n=4;
		int result=0;
		
		result=n%2==0 ?10 : 20;
		System.out.println(result);

	}

}
