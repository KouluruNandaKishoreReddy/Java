import java.util.*;
public class print_n_numbers_using_whlie {

	public static void main(String[] args) {
		
		Scanner sc= new Scanner(System.in);
		System.out.println("Enter the number n: ");
		int n = sc.nextInt();
		int i=1;
		
		
		while(i< n)
		{
		System.out.println(i);
		i++;
		}
		
		

	}

}
